package exercicioslogica;

public class Ex51 {
    public static void main(String[] args) {
//        51) Faça um programa que exiba os números de 1 a 10 usando o loop "do-while".
        int numero = 1;

        do {
            System.out.println(numero);
            numero++;
        } while (numero <= 10);
    }
}

